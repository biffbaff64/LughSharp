        protected int GetAllocationLength( Header header )
        {
            if ( header.HVersion == Header.MPEG1 )
            {
                var channelBitrate = header.HBitrateIndex;

                // calculate bitrate per channel:
                if ( header.HMode != Header.SINGLE_CHANNEL )
                {
                    if ( channelBitrate == 4 )
                    {
                        channelBitrate = 1;
                    }
                    else
                    {
                        channelBitrate -= 4;
                    }
                }

                if ( ( channelBitrate == 1 ) || ( channelBitrate == 2 ) )
                {
                    // table 3-B.2c or 3-B.2d
                    if ( subbandNumber <= 1 )
                    {
                        return 4;
                    }
                    else
                    {
                        return 3;
                    }
                }
                else
                {
                    // tables 3-B.2a or 3-B.2b
                    if ( subbandNumber <= 10 )
                    {
                        return 4;
                    }
                    else if ( subbandNumber <= 22 )
                    {
                        return 3;
                    }
                    else
                    {
                        return 2;
                    }
                }
            }
            else // table B.1 of ISO/IEC 13818-3
            {
                if ( subbandNumber <= 3 )
                {
                    return 4;
                }
                else if ( subbandNumber <= 10 )
                {
                    return 3;
                }
                else
                {
                    return 2;
                }
            }
        }

        protected void PrepareSampleReading( Header header,
                                             int allocation_,
                                             int channel,
                                             float[] factor_,
                                             int[] codelength,
                                             float[] c_,
                                             float[] d_ )
        {
            var channelBitrate = header.HBitrateIndex;

            // calculate bitrate per channel:
            if ( header.HMode != Header.SINGLE_CHANNEL )
            {
                if ( channelBitrate == 4 )
                {
                    channelBitrate = 1;
                }
                else
                {
                    channelBitrate -= 4;
                }
            }

            if ( ( channelBitrate == 1 ) || ( channelBitrate == 2 ) )
            {
                // table 3-B.2c or 3-B.2d
                groupingTable[ channel ] = tableCdGroupingtables[ allocation_ ];
                factor[ 0 ]              = tableCdFactor[ allocation_ ];
                codelength[ 0 ]          = tableCdCodelength[ allocation_ ];
                c[ 0 ]                   = tableCdC[ allocation_ ];
                d[ 0 ]                   = tableCdD[ allocation_ ];
            }
            else // tables 3-B.2a or 3-B.2b
            {
                if ( subbandNumber <= 2 )
                {
                    groupingTable[ channel ] = tableAb1Groupingtables[ allocation_ ];
                    factor[ 0 ]              = tableAb1Factor[ allocation_ ];
                    codelength[ 0 ]          = tableAb1Codelength[ allocation_ ];
                    c[ 0 ]                   = tableAb1C[ allocation_ ];
                    d[ 0 ]                   = TableAb1D[ allocation_ ];
                }
                else
                {
                    groupingTable[ channel ] = tableAb234Groupingtables[ allocation_ ];

                    if ( subbandNumber <= 10 )
                    {
                        factor[ 0 ]     = tableAb2Factor[ allocation_ ];
                        codelength[ 0 ] = tableAb2Codelength[ allocation_ ];
                        c[ 0 ]          = tableAb2C[ allocation_ ];
                        d[ 0 ]          = tableAb2D[ allocation_ ];
                    }
                    else if ( subbandNumber <= 22 )
                    {
                        factor[ 0 ]     = tableAb3Factor[ allocation_ ];
                        codelength[ 0 ] = tableAb3Codelength[ allocation_ ];
                        c[ 0 ]          = tableAb3C[ allocation_ ];
                        d[ 0 ]          = tableAb3D[ allocation_ ];
                    }
                    else
                    {
                        factor[ 0 ]     = tableAb4Factor[ allocation_ ];
                        codelength[ 0 ] = tableAb4Codelength[ allocation_ ];
                        c[ 0 ]          = tableAb4C[ allocation_ ];
                        d[ 0 ]          = tableAb4D[ allocation_ ];
                    }
                }
            }
        }

        public override void ReadAllocation( Bitstream? stream, Header? header, Crc16? crc )
        {
            ArgumentNullException.ThrowIfNull( stream );
            ArgumentNullException.ThrowIfNull( header );

            var length = GetAllocationLength( header );
            allocation = stream.GetBits( length );

            if ( crc != null )
            {
                crc.AddBits( allocation, length );
            }
        }

        public virtual void ReadScalefactorSelection( Bitstream? stream, Crc16? crc )
        {
            if ( allocation != 0 )
            {
                scfsi = stream!.GetBits( 2 );

                if ( crc != null )
                {
                    crc.AddBits( scfsi, 2 );
                }
            }
        }

        public override void ReadScaleFactor( Bitstream? stream, Header? header )
        {
            ArgumentNullException.ThrowIfNull( stream );
            ArgumentNullException.ThrowIfNull( header );

            if ( allocation != 0 )
            {
                switch ( scfsi )
                {
                    case 0:
                        scaleFactor1 = ScaleFactors[ stream.GetBits( 6 ) ];
                        scaleFactor2 = ScaleFactors[ stream.GetBits( 6 ) ];
                        scaleFactor3 = ScaleFactors[ stream.GetBits( 6 ) ];

                        break;

                    case 1:
                        scaleFactor1 = scaleFactor2 = ScaleFactors[ stream.GetBits( 6 ) ];
                        scaleFactor3 = ScaleFactors[ stream.GetBits( 6 ) ];

                        break;

                    case 2:
                        scaleFactor1 = scaleFactor2 = scaleFactor3 = ScaleFactors[ stream.GetBits( 6 ) ];

                        break;

                    case 3:
                        scaleFactor1 = ScaleFactors[ stream.GetBits( 6 ) ];
                        scaleFactor2 = scaleFactor3 = ScaleFactors[ stream.GetBits( 6 ) ];

                        break;
                }

                PrepareSampleReading( header, allocation, 0, factor, codeLength, c, d );
            }
        }

        public override bool ReadSampleData( Bitstream? stream )
        {
            ArgumentNullException.ThrowIfNull( stream );

            if ( allocation != 0 )
            {
                if ( groupingTable[ 0 ] != null )
                {
                    var samplecode = stream.GetBits( codeLength[ 0 ] );

                    // create requantized samples:
                    samplecode += samplecode << 1;

                    var target = samples;
                    var source = groupingTable[ 0 ];
                    var tmp    = 0;
                    var temp   = samplecode;

                    Debug.Assert( source != null );

                    if ( temp > ( source.Length - 3 ) )
                    {
                        temp = source.Length - 3;
                    }

                    target[ tmp ] = source[ temp ];

                    temp++;
                    tmp++;
                    target[ tmp ] = source[ temp ];

                    temp++;
                    tmp++;
                    target[ tmp ] = source[ temp ];
                }
                else
                {
                    samples[ 0 ] = ( float )( ( stream.GetBits( codeLength[ 0 ] ) * factor[ 0 ] ) - 1.0 );
                    samples[ 1 ] = ( float )( ( stream.GetBits( codeLength[ 0 ] ) * factor[ 0 ] ) - 1.0 );
                    samples[ 2 ] = ( float )( ( stream.GetBits( codeLength[ 0 ] ) * factor[ 0 ] ) - 1.0 );
                }
            }

            sampleNumber = 0;

            if ( ++groupNumber == 12 )
            {
                return true;
            }
            else
            {
                return false;
            }
        }

        public override bool PutNextSample( int channels,
                                            SynthesisFilter? filter1,
                                            SynthesisFilter? filter2 )
        {
            if ( ( allocation != 0 ) && ( channels != OutputChannels.RIGHT_CHANNEL ) )
            {
                var sample = samples[ sampleNumber ];

                if ( groupingTable[ 0 ] == null )
                {
                    sample = ( sample + d[ 0 ] ) * c[ 0 ];
                }

                if ( groupNumber <= 4 )
                {
                    sample *= scaleFactor1;
                }
                else if ( groupNumber <= 8 )
                {
                    sample *= scaleFactor2;
                }
                else
                {
                    sample *= scaleFactor3;
                }

                filter1?.InputSample( sample, subbandNumber );
            }

            if ( ++sampleNumber == 3 )
            {
                return true;
            }
            else
            {
                return false;
            }
        }
    }

