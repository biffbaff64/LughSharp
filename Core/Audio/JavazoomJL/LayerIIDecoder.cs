                if ( groupingTable[ 0 ] == null )
                {
                    sample = ( sample + d[ 0 ] ) * c[ 0 ];
                }

                if ( channels == OutputChannels.BOTH_CHANNELS )
                {
                    var sample2 = sample;

                    if ( groupNumber <= 4 )
                    {
                        sample  *= scaleFactor1;
                        sample2 *= channel2Scalefactor1;
                    }
                    else if ( groupNumber <= 8 )
                    {
                        sample  *= scaleFactor2;
                        sample2 *= channel2Scalefactor2;
                    }
                    else
                    {
                        sample  *= scaleFactor3;
                        sample2 *= channel2Scalefactor3;
                    }

                    filter1?.InputSample( sample, subbandNumber );
                    filter2?.InputSample( sample2, subbandNumber );
                }
                else if ( channels == OutputChannels.LEFT_CHANNEL )
                {
                    if ( groupNumber <= 4 )
                    {
                        sample *= scaleFactor1;
                    }
                    else if ( groupNumber <= 8 )
                    {
                        sample *= scaleFactor2;
                    }
                    else
                    {
                        sample *= scaleFactor3;
                    }

                    filter1?.InputSample( sample, subbandNumber );
                }
                else
                {
                    if ( groupNumber <= 4 )
                    {
                        sample *= channel2Scalefactor1;
                    }
                    else if ( groupNumber <= 8 )
                    {
                        sample *= channel2Scalefactor2;
                    }
                    else
                    {
                        sample *= channel2Scalefactor3;
                    }

                    filter1?.InputSample( sample, subbandNumber );
                }
            }

            if ( ++sampleNumber == 3 )
            {
                return true;
            }
            else
            {
                return false;
            }
        }
    };

    /// <summary>
    /// Class for layer II subbands in stereo mode.
    /// </summary>
    [PublicAPI]
    public class SubbandLayer2Stereo : SubbandLayer2
    {
        protected int     channel2Allocation;
        protected int     channel2Scfsi;
        protected float   channel2Scalefactor1;
        protected float   channel2Scalefactor2;
        protected float   channel2Scalefactor3;
        protected int[]   channel2Codelength = { 0 };
        protected float[] channel2Factor     = { 0 };
        protected float[] channel2Samples;
        protected float[] channel2C = { 0 };
        protected float[] channel2D = { 0 };

        public SubbandLayer2Stereo( int subbandnumber )
            : base( subbandnumber )
        {
            channel2Samples = new float[ 3 ];
        }

        public override void ReadAllocation( Bitstream? stream, Header? header, Crc16? crc )
        {
            ArgumentNullException.ThrowIfNull( stream );
            ArgumentNullException.ThrowIfNull( header );

            var length = GetAllocationLength( header );

            allocation         = stream.GetBits( length );
            channel2Allocation = stream.GetBits( length );

            if ( crc != null )
            {
                crc.AddBits( allocation, length );
                crc.AddBits( channel2Allocation, length );
            }
        }

        public override void ReadScalefactorSelection( Bitstream? stream, Crc16? crc )
        {
            if ( allocation != 0 )
            {
                scfsi = stream!.GetBits( 2 );

                if ( crc != null )
                {
                    crc.AddBits( scfsi, 2 );
                }
            }

            if ( channel2Allocation != 0 )
            {
                channel2Scfsi = stream!.GetBits( 2 );

                if ( crc != null )
                {
                    crc.AddBits( channel2Scfsi, 2 );
                }
            }
        }

        public override void ReadScaleFactor( Bitstream? stream, Header? header )
        {
            ArgumentNullException.ThrowIfNull( stream );

            base.ReadScaleFactor( stream, header );

            if ( channel2Allocation != 0 )
            {
                switch ( channel2Scfsi )
                {
                    case 0:
                        channel2Scalefactor1 = ScaleFactors[ stream.GetBits( 6 ) ];
                        channel2Scalefactor2 = ScaleFactors[ stream.GetBits( 6 ) ];
                        channel2Scalefactor3 = ScaleFactors[ stream.GetBits( 6 ) ];

                        break;

                    case 1:
                        channel2Scalefactor1 = ScaleFactors[ stream.GetBits( 6 ) ];
                        channel2Scalefactor2 = channel2Scalefactor1;
                        channel2Scalefactor3 = ScaleFactors[ stream.GetBits( 6 ) ];

                        break;

                    case 2:
                        channel2Scalefactor1 = ScaleFactors[ stream.GetBits( 6 ) ];
                        channel2Scalefactor2 = channel2Scalefactor1;
                        channel2Scalefactor3 = channel2Scalefactor1;

                        break;

                    case 3:
                        channel2Scalefactor1 = ScaleFactors[ stream.GetBits( 6 ) ];
                        channel2Scalefactor2 = ScaleFactors[ stream.GetBits( 6 ) ];
                        channel2Scalefactor3 = channel2Scalefactor2;

                        break;
                }

                PrepareSampleReading( header!,
                                      channel2Allocation,
                                      1,
                                      channel2Factor,
                                      channel2Codelength,
                                      channel2C,
                                      channel2D );
            }
        }

        public override bool ReadSampleData( Bitstream? stream )
        {
            ArgumentNullException.ThrowIfNull( stream );

            var returnvalue = base.ReadSampleData( stream );

            if ( channel2Allocation != 0 )
            {
                if ( groupingTable[ 1 ] != null )
                {
                    var samplecode = stream.GetBits( channel2Codelength[ 0 ] );

                    // create requantized samples:
                    samplecode += samplecode << 1;

                    var target = channel2Samples;
                    var source = groupingTable[ 1 ];
                    var tmp    = 0;
                    var temp   = samplecode;

                    target[ tmp ] = source![ temp ];

                    temp++;
                    tmp++;
                    target[ tmp ] = source[ temp ];

                    temp++;
                    tmp++;
                    target[ tmp ] = source[ temp ];

                }
                else
                {
                    channel2Samples[ 0 ]
                        = ( float )( ( stream.GetBits( channel2Codelength[ 0 ] ) * channel2Factor[ 0 ] ) - 1.0 );

                    channel2Samples[ 1 ]
                        = ( float )( ( stream.GetBits( channel2Codelength[ 0 ] ) * channel2Factor[ 0 ] ) - 1.0 );

                    channel2Samples[ 2 ]
                        = ( float )( ( stream.GetBits( channel2Codelength[ 0 ] ) * channel2Factor[ 0 ] ) - 1.0 );
                }
            }

            return returnvalue;
        }

        public override bool PutNextSample( int channels,
                                            SynthesisFilter? filter1,
                                            SynthesisFilter? filter2 )
        {
            var returnvalue = base.PutNextSample( channels, filter1, filter2 );

            if ( ( channel2Allocation != 0 ) && ( channels != OutputChannels.LEFT_CHANNEL ) )
            {
                var sample = channel2Samples[ sampleNumber - 1 ];

                if ( groupingTable[ 1 ] == null )
                {
                    sample = ( sample + channel2D[ 0 ] ) * channel2C[ 0 ];
                }

                if ( groupNumber <= 4 )
                {
                    sample *= channel2Scalefactor1;
                }
                else if ( groupNumber <= 8 )
                {
                    sample *= channel2Scalefactor2;
                }
                else
                {
                    sample *= channel2Scalefactor3;
                }

                if ( channels == OutputChannels.BOTH_CHANNELS )
                {
                    filter2?.InputSample( sample, subbandNumber );
                }
                else
                {
                    filter1?.InputSample( sample, subbandNumber );
                }
            }

            return returnvalue;
        }
    }
