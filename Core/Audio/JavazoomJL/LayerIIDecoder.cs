namespace LibGDXSharp.Core.Audio.JavazoomJL;

/// <summary>
/// Implements decoding of MPEG Audio Layer II frames.
/// </summary>
[PublicAPI]
public class LayerIIDecoder : LayerIDecoder
{
    public LayerIIDecoder( Bitstream stream0,
                          Header header0,
                          SynthesisFilter? filterA,
                          SynthesisFilter? filterB,
                          OutputBuffer? output,
                          int channel )
        : base( stream0, header0, filterA, filterB, output, channel )
    {
    }

    protected override void CreateSubbands()
    {
        int i;

        if ( mode == Header.SINGLE_CHANNEL )
        {
            for ( i = 0; i < numSubbands; ++i )
            {
                subbands![ i ] = new SubbandLayer2( i );
            }
        }
        else if ( mode == Header.JOINT_STEREO )
        {
            for ( i = 0; i < header!.HIntensityStereoBound; ++i )
            {
                subbands![ i ] = new SubbandLayer2Stereo( i );
            }

            for ( ; i < numSubbands; ++i )
            {
                subbands![ i ] = new SubbandLayer2IntensityStereo( i );
            }
        }
        else
        {
            for ( i = 0; i < numSubbands; ++i )
            {
                subbands![ i ] = new SubbandLayer2Stereo( i );
            }
        }
    }

    protected void ReadScalefactorSelection()
    {
        for ( var i = 0; i < numSubbands; ++i )
        {
            ( ( SubbandLayer2 )subbands![ i ] ).ReadScalefactorSelection( stream, crc );
        }
    }

