    {
        public int KeyCode { get; set; }

        private TextField? _tf;

        public void Create( TextField tf )
        {
            _tf = tf;

            _tf._keyRepeatTokenSource       = new CancellationTokenSource();
            _tf._keyRepeatCancellationToken = _tf._keyRepeatTokenSource.Token;

