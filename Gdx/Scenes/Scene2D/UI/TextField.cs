        {
        }

        public TextFieldStyle( BitmapFont font,
                               Color fontColor,
                               IDrawable? cursor,
                               IDrawable? selection,
                               IDrawable? background )
        {
            Font       = font;
            FontColor  = fontColor;
            Cursor     = cursor;
            Selection  = selection;
            Background = background;
        }

